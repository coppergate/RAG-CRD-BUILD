CREATE INDEX IF NOT EXISTS idx_tag_name ON tag(tag_name);
CREATE INDEX IF NOT EXISTS idx_code_ingestion_bucket ON code_ingestion(s3_bucket_id);

-- Iteration 7: Long-term Memory Support

-- 1) memory_items (Main memory storage)
CREATE TABLE IF NOT EXISTS memory_items (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    project_id BIGINT REFERENCES projects(id) ON DELETE CASCADE,
    session_id BIGINT REFERENCES sessions(session_id) ON DELETE SET NULL,
    user_id UUID,
    memory_type TEXT NOT NULL,
    summary TEXT NOT NULL,
    content TEXT,
    salience DOUBLE PRECISION NOT NULL DEFAULT 0.0,
    retention_score DOUBLE PRECISION NOT NULL DEFAULT 1.0,
    decay_state JSONB DEFAULT '{}'::jsonb,
    status TEXT NOT NULL DEFAULT 'active', -- active, pruned, archived
    pinned BOOLEAN NOT NULL DEFAULT FALSE,
    expires_at TIMESTAMPTZ,
    metadata JSONB DEFAULT '{}'::jsonb,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- 2) memory_links (Provenance and association)
CREATE TABLE IF NOT EXISTS memory_links (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    memory_item_id BIGINT REFERENCES memory_items(id) ON DELETE CASCADE,
    source_message_ids JSONB DEFAULT '[]'::jsonb,
    ingestion_ids JSONB DEFAULT '[]'::jsonb,
    tags JSONB DEFAULT '[]'::jsonb,
    metadata JSONB DEFAULT '{}'::jsonb,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 3) memory_events (Audit and lifecycle)
CREATE TABLE IF NOT EXISTS memory_events (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    memory_item_id BIGINT REFERENCES memory_items(id) ON DELETE SET NULL,
    session_id BIGINT REFERENCES sessions(session_id) ON DELETE CASCADE,
    event_type TEXT NOT NULL,
    event_data JSONB DEFAULT '{}'::jsonb,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_memory_items_session ON memory_items(session_id);
CREATE INDEX IF NOT EXISTS idx_memory_items_status ON memory_items(status);
CREATE INDEX IF NOT EXISTS idx_memory_links_item ON memory_links(memory_item_id);
CREATE INDEX IF NOT EXISTS idx_memory_events_item ON memory_events(memory_item_id);
CREATE INDEX IF NOT EXISTS idx_memory_events_session ON memory_events(session_id);
