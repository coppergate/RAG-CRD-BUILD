CREATE TABLE IF NOT EXISTS retrieval_logs (
    log_id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    session_id BIGINT REFERENCES sessions(session_id) ON DELETE CASCADE,
    message_id UUID, -- Optional linkage to message/prompt
    retrieved_chunks JSONB, -- List of paths and chunks
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Iteration 2: Ingestion and Tagging Support

-- 6. Code Ingestion Tracking
CREATE TABLE IF NOT EXISTS code_ingestion (
    ingestion_id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    s3_bucket_id TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 7. Tag Definition
CREATE TABLE IF NOT EXISTS tag (
  tag_id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  tag_name TEXT NOT NULL UNIQUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 8. Ingestion to Tag Mapping
CREATE TABLE IF NOT EXISTS code_ingestion_tag (
    ingestion_id BIGINT REFERENCES code_ingestion(ingestion_id) ON DELETE CASCADE,
    tag_id BIGINT REFERENCES tag(tag_id) ON DELETE CASCADE,
    PRIMARY KEY (ingestion_id, tag_id)
);

-- 9. Code Embedding Index (Metadata backup)
CREATE TABLE IF NOT EXISTS code_embedding (
    embedding_id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    ingestion_id BIGINT REFERENCES code_ingestion(ingestion_id) ON DELETE CASCADE,
    embedding_vector JSONB, -- Changed from REAL[] for Ent compatibility
    metadata JSONB,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 10. Embedding to Tag Mapping
CREATE TABLE IF NOT EXISTS code_embedding_tag (
    embedding_id BIGINT REFERENCES code_embedding(embedding_id) ON DELETE CASCADE,
    tag_id BIGINT REFERENCES tag(tag_id) ON DELETE CASCADE,
    PRIMARY KEY (embedding_id, tag_id)
);

-- 11. Session to Tag Association
CREATE TABLE IF NOT EXISTS session_tag (
    session_id BIGINT REFERENCES sessions(session_id) ON DELETE CASCADE,
    tag_id BIGINT REFERENCES tag(tag_id) ON DELETE CASCADE,
    PRIMARY KEY (session_id, tag_id)
);

-- 12. Responses (hypertable)
CREATE TABLE IF NOT EXISTS responses (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    response_id UUID DEFAULT gen_random_uuid(),
    prompt_id BIGINT, -- references prompts(id) logically
    session_id BIGINT REFERENCES sessions(session_id) ON DELETE CASCADE,
    content TEXT NOT NULL,
    planning_response TEXT,
    sequence_number INTEGER NOT NULL,
    model_name TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    metadata JSONB,
    PRIMARY KEY (id, created_at)
);
SELECT create_hypertable('responses', 'created_at', if_not_exists => TRUE);

-- 12a. Response indexes
CREATE INDEX IF NOT EXISTS idx_responses_prompt ON responses(prompt_id, sequence_number);
CREATE INDEX IF NOT EXISTS idx_responses_response ON responses(response_id, sequence_number);
CREATE INDEX IF NOT EXISTS idx_responses_session ON responses(session_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_responses_content_gin ON responses USING GIN (to_tsvector('english', content));

-- Inference Nodes (Dimension)
CREATE TABLE IF NOT EXISTS inference_nodes (
    node_id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    hostname TEXT UNIQUE NOT NULL,
    ip_address TEXT,
    gpu_model TEXT,
    total_vram_mb INTEGER,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Model Definitions (Dimension)
CREATE TABLE IF NOT EXISTS model_definitions (
    model_id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    model_name TEXT UNIQUE NOT NULL, -- e.g. "llama3.1:8b"
    family TEXT, -- llama, granite, etc.
    parameters_billions REAL,
    quantization TEXT,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Model Execution Metrics (Fact Table)
CREATE TABLE IF NOT EXISTS model_execution_metrics (
    metric_id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    response_id BIGINT, -- Logical link to responses(id)
    session_id BIGINT REFERENCES sessions(session_id) ON DELETE CASCADE,
    node_id BIGINT REFERENCES inference_nodes(node_id),
    model_id BIGINT REFERENCES model_definitions(model_id),
    
    -- Token counts
    prompt_tokens INTEGER,
    completion_tokens INTEGER,
    total_tokens INTEGER GENERATED ALWAYS AS (prompt_tokens + completion_tokens) STORED,
    
    -- Durations (microseconds for precision, converted to ms in views)
    total_duration_usec BIGINT,
    load_duration_usec BIGINT,
    prompt_eval_duration_usec BIGINT,
    eval_duration_usec BIGINT,
    
    -- Derived Metrics
    tokens_per_second REAL,
    
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (metric_id, created_at)
);
SELECT create_hypertable('model_execution_metrics', 'created_at', if_not_exists => TRUE);

-- Indexes for performance comparison
CREATE INDEX IF NOT EXISTS idx_metrics_model ON model_execution_metrics(model_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_metrics_session ON model_execution_metrics(session_id);
CREATE INDEX IF NOT EXISTS idx_metrics_node ON model_execution_metrics(node_id);

CREATE OR REPLACE PROCEDURE expire_old_sessions(job_id int, config jsonb)
LANGUAGE plpgsql
AS $$
BEGIN
    DELETE FROM sessions
    WHERE last_active_at < now() - INTERVAL '24 hours';
END
$$;

-- Register cleanup job (every 1 hour)
DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM timescaledb_information.jobs WHERE proc_name = 'expire_old_sessions') THEN
        PERFORM add_job('expire_old_sessions', '1 hour');
    END IF;
END
$$;

