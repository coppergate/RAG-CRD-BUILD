-- RAG Session Management Schema (Unified)
-- Using TimescaleDB for time-series chat history
-- NOTE: This unified schema supersedes prior iteration-specific DDLs.

CREATE TABLE IF NOT EXISTS projects (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    description TEXT,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

CREATE TABLE IF NOT EXISTS sessions (
    session_id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    project_id BIGINT REFERENCES projects(id) ON DELETE CASCADE,
    name TEXT UNIQUE,
    description TEXT,
    metadata JSONB, -- For any extra info
    user_id UUID,
    created_at TIMESTAMPTZ DEFAULT now(),
    last_active_at TIMESTAMPTZ DEFAULT now()
);

-- 3. Prompts (hypertable)
CREATE TABLE IF NOT EXISTS prompts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    prompt_id UUID DEFAULT gen_random_uuid(),
    session_id BIGINT REFERENCES sessions(session_id) ON DELETE CASCADE,
    content TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    metadata JSONB,
    PRIMARY KEY (id, created_at)
);
SELECT create_hypertable('prompts', 'created_at', if_not_exists => TRUE);

-- 3a. Prompt indexes
CREATE INDEX IF NOT EXISTS idx_prompts_session ON prompts(session_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_prompts_prompt ON prompts(prompt_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_prompts_content_gin ON prompts USING GIN (to_tsvector('english', content));

-- Retention policy example; adjust as needed per hypertable
